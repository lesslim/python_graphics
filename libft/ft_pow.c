#include "libft.h"

long		ft_pow(int base, int exp)
{
	long long	n;

	n = 1;
	while (exp > 0)
	{
		n *= base;
		if (n > LONG_MAX)
			return (0);
		exp--;
	}
	return ((long)n);
}
