#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (s)
	{
		while (*s != (char)c && *s != '\0')
			s++;
		if ((char)c == '\0' || *s == (char)c)
			return ((char *)s);
	}
	return (NULL);
}
