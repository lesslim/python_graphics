#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*tmp;

	tmp = s1;
	while (*tmp != '\0')
		tmp++;
	while (*s2 != '\0' && n-- != 0)
		*tmp++ = *s2++;
	*tmp = '\0';
	return (s1);
}
